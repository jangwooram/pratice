package pattern;

public class MathEx1 {
    public static void main(String[] args) {
        System.out.println(Math.abs(-5)); //절대값
        System.out.println(Math.ceil(5.3)); //올림
        System.out.println(Math.floor(2.8)); //내림
        System.out.println(Math.round(4.6)); //반올림
        System.out.println(Math.max(56,876));// 큰 값을 반환
        System.out.println(Math.min(45,876));// 작은값
        System.out.println(Math.random()); //0~1 사이의 난수
    }
}
